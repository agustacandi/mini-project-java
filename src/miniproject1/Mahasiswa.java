    private void setDataMahasiswa(String nim, String password) throws SQLException {
        try {
            String getAllData = "SELECT m.nim, m.nama, m.jurusan, m.alamat, m.phone, a.password FROM mhs m "
                    + "JOIN akun a "
                    + "ON m.nim = a.nim "
                    + "WHERE m.nim ='" + nim + "'";
            Connection conn = (Connection) Config.configDB();
            PreparedStatement pst = conn.prepareStatement(getAllData);
            ResultSet rs = pst.executeQuery(getAllData);
            if (rs.next()) {
                txt_nim.setText(rs.getString("nim"));
                txt_nama.setText(rs.getString("nama"));
                String jurusan = rs.getString("jurusan");
                if (jurusan == null) {
                    box_jurusan.setSelectedIndex(0);
                } else {
                    if (jurusan.equalsIgnoreCase("Produksi Pertanian")) {
                    box_jurusan.setSelectedIndex(0);
                } else if (jurusan.equalsIgnoreCase("Teknologi Pertanian")) {
                    box_jurusan.setSelectedIndex(1);
                } else if (jurusan.equalsIgnoreCase("Peternakan")) {
                    box_jurusan.setSelectedIndex(2);
                } else if (jurusan.equalsIgnoreCase("Manajemen Agribisnis")) {
                    box_jurusan.setSelectedIndex(3);
                } else if (jurusan.equalsIgnoreCase("Teknologi Informasi")) {
                    box_jurusan.setSelectedIndex(4);
                } else if (jurusan.equalsIgnoreCase("Bahasa Komunikasi dan Pariwisata")) {
                    box_jurusan.setSelectedIndex(5);
                } else if (jurusan.equalsIgnoreCase("Kesehatan")) {
                    box_jurusan.setSelectedIndex(6);
                } else {
                    box_jurusan.setSelectedIndex(7);
                }
                }
                txt_alamat.setText(rs.getString("alamat"));
                txt_phone.setText(rs.getString("phone"));
                txt_password.setText(rs.getString("password"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

